GENERATED_MARKER = "Generated by ops-script-maintenance scaffold."


@dataclass(frozen=True)
class NodePackage:
    relative_dir: str
    has_dev: bool
    has_build: bool
    has_test: bool


@dataclass(frozen=True)
class ProjectProfile:
    has_python: bool
    python_requirement: str
    has_pyproject: bool
    has_requirements: bool
    node_packages: tuple[NodePackage, ...]

    @property
    def name(self) -> str:
        parts: list[str] = []
        if self.has_python:
            parts.append("python")
        if self.node_packages:
            parts.append("node")
        return "-".join(parts) if parts else "generic"


def discover_profile(root: Path) -> ProjectProfile:
    has_pyproject = (root / "pyproject.toml").exists()
    has_requirements = (root / "requirements.txt").exists()
    has_python = has_pyproject or has_requirements or (root / "setup.py").exists()
    python_requirement = _read_pyproject_python(root) if has_pyproject else ""
    return ProjectProfile(
        has_python=has_python,
        python_requirement=python_requirement,
        has_pyproject=has_pyproject,
        has_requirements=has_requirements,
        node_packages=tuple(_discover_node_packages(root)),
    )


def _read_pyproject_python(root: Path) -> str:
    try:
        text = (root / "pyproject.toml").read_text(encoding="utf-8")
    except FileNotFoundError:
        return ""
    for line in text.splitlines():
        stripped = line.strip()
        if stripped.startswith("requires-python") and "=" in stripped:
            return stripped.split("=", 1)[1].strip().strip('"').strip("'")
    return ""


def _discover_node_packages(root: Path) -> list[NodePackage]:
    candidates = [Path(".")]
    candidates.extend(Path(name) for name in ("web", "frontend", "client", "app", "ui"))
    packages: list[NodePackage] = []
    seen: set[str] = set()
    for relative_dir in candidates:
        package_path = root / relative_dir / "package.json"
        if not package_path.exists():
            continue
        normalized = relative_dir.as_posix()
        if normalized in seen:
            continue
        seen.add(normalized)
        payload = _load_json(package_path)
        scripts = payload.get("scripts", {}) if isinstance(payload, dict) else {}
        scripts = scripts if isinstance(scripts, dict) else {}
        packages.append(
            NodePackage(
                relative_dir=normalized,
                has_dev="dev" in scripts,
                has_build="build" in scripts,
                has_test="test" in scripts,
            )
        )
    return packages


def _load_json(path: Path) -> dict[str, object]:
    try:
        return json.loads(path.read_text(encoding="utf-8"))
    except (json.JSONDecodeError, OSError):
        return {}


def render_manage_script(profile: ProjectProfile) -> str:
    node_dirs = tuple(item.relative_dir for item in profile.node_packages)
    node_dev_dirs = tuple(item.relative_dir for item in profile.node_packages if item.has_dev)
    node_build_dirs = tuple(item.relative_dir for item in profile.node_packages if item.has_build)
    node_test_dirs = tuple(item.relative_dir for item in profile.node_packages if item.has_test)
    lines = [
        "#!/usr/bin/env bash",
        "",
        "set -euo pipefail",
        "",
        f"# {GENERATED_MARKER}",
        "# Update with: python3 .codex/skills/ops-script-maintenance/scripts/scaffold_manage.py --root .",
        "",
        f"PROJECT_PROFILE={shlex.quote(profile.name)}",
        f"HAS_PYTHON={1 if profile.has_python else 0}",
        f"PYTHON_REQUIREMENT={shlex.quote(profile.python_requirement)}",
        f"PYTHON_HAS_PYPROJECT={1 if profile.has_pyproject else 0}",
        f"PYTHON_HAS_REQUIREMENTS={1 if profile.has_requirements else 0}",
        f"NODE_PACKAGE_DIRS={_bash_array(node_dirs)}",
        f"NODE_DEV_DIRS={_bash_array(node_dev_dirs)}",
        f"NODE_BUILD_DIRS={_bash_array(node_build_dirs)}",
        f"NODE_TEST_DIRS={_bash_array(node_test_dirs)}",
        MANAGE_BODY.rstrip(),
        "",
    ]
    return "\n".join(lines)


def _bash_array(values: tuple[str, ...]) -> str:
    if not values:
        return "()"
    return "(" + " ".join(shlex.quote(value) for value in values) + ")"


