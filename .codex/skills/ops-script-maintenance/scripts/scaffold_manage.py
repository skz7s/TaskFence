def render_wrapper(command: str) -> str:
    exec_line = 'exec bash "${' + f'THIS_DIR}}/manage.sh" {command} "$@"'
    return "\n".join(
        [
            "#!/usr/bin/env bash",
            "",
            "set -euo pipefail",
            "",
            f"# {GENERATED_MARKER}",
            'THIS_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"',
            exec_line,
            "",
        ]
    )


def scaffold(
    *,
    root: Path,
    force: bool = False,
    dry_run: bool = False,
    with_wrappers: bool = False,
) -> tuple[Path, ...]:
    root = root.expanduser().resolve()
    profile = discover_profile(root)
    deploy_dir = root / "deploy"
    files: dict[Path, str] = {
        deploy_dir / "manage.sh": render_manage_script(profile),
    }
    if with_wrappers:
        files[deploy_dir / "setup.sh"] = render_wrapper("setup")
        files[deploy_dir / "deploy.sh"] = render_wrapper("build")
        files[deploy_dir / "build.sh"] = render_wrapper("build")

    written: list[Path] = []
    for path, content in files.items():
        if path.exists() and not force and not _is_generated(path):
            raise SystemExit(
                f"refusing to overwrite non-scaffolded file: {path}. "
                "Pass --force after reviewing the existing script."
            )
        if dry_run:
            written.append(path)
            continue
        path.parent.mkdir(parents=True, exist_ok=True)
        path.write_text(content, encoding="utf-8")
        _make_executable(path)
        written.append(path)
    return tuple(written)


def _is_generated(path: Path) -> bool:
    try:
        return GENERATED_MARKER in path.read_text(encoding="utf-8", errors="ignore")
    except OSError:
        return False


def _make_executable(path: Path) -> None:
    mode = path.stat().st_mode
    path.chmod(mode | stat.S_IXUSR | stat.S_IXGRP | stat.S_IXOTH)


def _run_bash_syntax_check(paths: tuple[Path, ...]) -> None:
    shell_paths = [path for path in paths if path.suffix == ".sh"]
    if not shell_paths:
        return
    result = subprocess.run(
        ["bash", "-n", *(str(path) for path in shell_paths)],
        text=True,
        capture_output=True,
        check=False,
    )
    if result.returncode != 0:
        raise SystemExit((result.stdout + result.stderr).strip())


def main() -> int:
    parser = argparse.ArgumentParser(
        description="Scaffold a project-local deploy/manage.sh from the repository structure."
    )
    parser.add_argument("--root", type=Path, default=Path.cwd(), help="target repository root")
    parser.add_argument("--force", action="store_true", help="overwrite existing scripts")
    parser.add_argument("--dry-run", action="store_true", help="show target paths without writing")
    wrapper_group = parser.add_mutually_exclusive_group()
    wrapper_group.add_argument(
        "--with-wrappers",
        action="store_true",
        help="also write legacy setup/deploy/build wrappers",
    )
    wrapper_group.add_argument(
        "--no-wrappers",
        action="store_true",
        help="deprecated no-op; deploy/manage.sh is the default output",
    )
    parser.add_argument(
        "--check",
        action="store_true",
        help="run bash -n on generated scripts after writing",
    )
    args = parser.parse_args()

    written = scaffold(
        root=args.root,
        force=args.force,
        dry_run=args.dry_run,
        with_wrappers=args.with_wrappers,
    )
    if args.check and not args.dry_run:
        _run_bash_syntax_check(written)
    for path in written:
        print(path)
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
